* AT $FEFD SO WE HAVE TO SPLIT OUR EXIT
* VECTORS OUT OF INTCX ROM
* REAL ROM HACK CODE AT $C600 ROM

SLOTCX      EQU   $C006       ;NORMAL SLOTCX BANK TOGGLE
MONITOR     EQU   $FF59
            TYP   BIN
            DSK   ROMP3
            ORG   $FECD       ;TAPE WRITE LOCATION
MONEXIT     STA   SLOTCX      ;TOGGLE BACK TO NORMAL
            JMP   MONITOR
