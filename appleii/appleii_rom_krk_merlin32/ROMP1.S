* ROM HACK PART 1
* WE INTERCEPT THE RESET VECTOR HERE
* THIS ROUTINE BANK SWITCHES TO THE
* INTCX ROM BANK AND JUMPS TO OUR
* REAL ROM HACK CODE AT $C600 ROM
* IF THE USER DOES NOT BREAK INTO THE
* THE MONITOR, THEN THE C600 ROUTINE (PART 2)
* WILL JMP BACK HERE FOR NORMAL EXIT

ROMHAK      EQU   $C600       ;OUR REAL CRACK IS HERE
INTCX       EQU   $C007       ;INTCX BANK TOGGLE ADDR
SLOTCX      EQU   $C006       ;NORMAL SLOTCX BANK TOGGLE
OLDVEC      EQU   $FA62

            TYP   BIN
            DSK   ROMP1
            ORG   $FEFD
            STA   INTCX       ;TOGGLE TO INTCX ROM BANK
            JMP   ROMHAK      ;AWAY WE GO
NORMAL      STA   SLOTCX      ;NORMAL EXIT
            JMP   OLDVEC
